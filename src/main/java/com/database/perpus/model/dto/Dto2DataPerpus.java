package com.database.perpus.model.dto;

public class Dto2DataPerpus {
    private int id;
    private char kode;
    private String nama;
    private char jk;
    private char jurusan;
    private char no_telp;
    private String alamat;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public char getKode() {
        return kode;
    }

    public void setKode(char kode) {
        this.kode = kode;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public char getJk() {
        return jk;
    }

    public void setJk(char jk) {
        this.jk = jk;
    }

    public char getJurusan() {
        return jurusan;
    }

    public void setJurusan(char jurusan) {
        this.jurusan = jurusan;
    }

    public char getNo_telp() {
        return no_telp;
    }

    public void setNo_telp(char no_telp) {
        this.no_telp = no_telp;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }
}
